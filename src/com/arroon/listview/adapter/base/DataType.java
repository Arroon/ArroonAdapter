package com.arroon.listview.adapter.base;

public class DataType {

	public String methodName;
	public int viewId;

	/**
	 * 
	 * @param methodName
	 *            方法名
	 * @param viewId
	 *            控件id
	 */
	public DataType(String methodName, int viewId) {
		super();
		this.methodName = methodName;
		this.viewId = viewId;
	}
}
